public class Dice {

	private int numSides;

	/**
	 * @param numSides
	 */
	public Dice(int numSides) {
		this.numSides = numSides;
	}

	/**
	 * @return the numSides
	 */
	public int getNumSides() {
		return numSides;
	}

	/**
	 * @param numSides the numSides to set
	 */
	public void setNumSides(int numSides) {
		this.numSides = numSides;
	}
	
	
	
}
